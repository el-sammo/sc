function insertSelections(entryName, selections) {
	db = new Mongo().getDB('supercontest');
	var entryId;

	var cursor = db.entries.find({name: entryName});
	while(cursor.hasNext()) {
		var entryData = cursor.next();
		entryId = (entryData._id.toString()).substring(10,34);

		db.selections.insert({
			entryId:  entryId,
			selections: selections,
			week: 17
		});
	}
}

