function reverseTotal(name,wWins,wLosses,wTies,wPoints,sWins,sLosses,sTies,sPoints) {
	db = new Mongo().getDB('supercontest');

	var cursor = db.entries.find({name: name});
	while(cursor.hasNext()) {
		var entryData = cursor.next();
		var standingsData = {};

		if(wWins !== 'No Selections') {
			sPoints -= wPoints;
		};

		standingsData.entryId = (entryData._id.toString()).substring(10,34);
		standingsData.name = entryData.name;
		standingsData.year = entryData.year;
		standingsData.total = sPoints;
		standingsData.week = 16;

		db.standings.insert(
			standingsData
		);
	}
}

